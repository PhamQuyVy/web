const sortedPhraseTokens = [...new Set(phraseTokens)].sort((a, b) => b.length - a.length);

function splitExample(example: string) {
  const [sentence, meaning = ""] = example.split("=");
  return {
    sentence: sentence.trim().replace(/[。？！?!，,、；;]/g, ""),
    meaning: meaning.trim(),
  };
}

function tokenizeSentence(sentence: string) {
  const tokens: string[] = [];
  let cursor = 0;

  while (cursor < sentence.length) {
    const match = sortedPhraseTokens.find((token) => sentence.startsWith(token, cursor));

    if (match) {
      tokens.push(match);
      cursor += match.length;
      continue;
    }

    tokens.push(sentence[cursor]);
    cursor += 1;
  }

  return tokens;
}

function buildTip(pattern: GrammarPattern) {
  const compactFormula = pattern.formula.replace(/\s+/g, " ").trim();

  if (compactFormula.includes("...")) {
    return `nhìn hai vế trong công thức "${compactFormula}", ghép vế trước rồi tới vế sau.`;
  }
  if (compactFormula.includes("+")) {
    return `ghép theo thứ tự công thức "${compactFormula}", đừng đảo vị trí hư từ.`;
  }
  return `tìm mảnh chứa cấu trúc chính "${compactFormula}" trước, rồi thêm chủ ngữ/tân ngữ.`;
}

export function buildSentenceDrillsFromGrammar(patterns: GrammarPattern[]): SentenceDrill[] {
  return patterns.flatMap((pattern) =>
    pattern.examples.slice(0, 3).map((example, index) => {
      const { sentence, meaning } = splitExample(example);
      const tokens = tokenizeSentence(sentence);

      return {
        id: `${pattern.id}-drill-${index + 1}`,
        title: `${pattern.title} (${index + 1})`,
        formula: pattern.formula,
        words: tokens,
        answer: tokens,
        meaning,
        tip: buildTip(pattern),
      };
    }),
  );
}
